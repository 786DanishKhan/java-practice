package FinalChatGpt;

public class MultiplesOfNumber {
    public static void main(String[] args) {
        int number = 5;
        int count = 10;
        for (int i = 1; i<=count; i++){
            System.out.println(number * i);
        }
        System.out.println(number);
        number = 12;
        System.out.println(number);
    }
}
